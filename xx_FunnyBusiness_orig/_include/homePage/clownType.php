		<h3>Auguste</h3>

			<p id="para">The auguste face base makeup color is a variation of pink, red, or tan. Features are exaggerated in size and are typically red and black in color. The mouth is thickly outlined with white (called the muzzle) as are the eyes. The auguste is dressed (appropriate to character) in either well-fitted garb or in a costume that does not fit - either oversize or too small is appropriate. Bold colors, large prints or patterns, and suspenders often characterize auguste costumes. - <span class="credit"><a href="http://www.wikipedia.org target="_blank" name="Wikipedia">Wikipedia.org</a></span></p>
			
